Student populateStudentFields() {                                   //method to populate Student fields 
        Student s = new Student();
        s.setName(txtName.getText());
        s.setID(txtxID.getText());
        s.setDepartnment(String.valueOf(comboDepartment.getSelectedItem()));
        s.setTelephone(txtMobileNumber.getText());
        s.setEmail(txtEmail.getText());
        s.setAddress(txtAddress.getText());
        s.setBirthday(txtBirthday.getText());
        s.setGender(String.valueOf(comboGender.getSelectedItem()));
        s.setNIC(txtxNIC.getText());
        s.setGPA(Float.parseFloat(txtGPA.getText()));
        s.setLanguages(txtLanguages.getText());
        s.setDescription(txtDescription.getText());
        s.setSkills(txtSkills.getText());
        s.setPassword(txtPassword.getText());
        if ("Approval Pending".equals(lblApproval.getText())) {
            s.setAdminAccepted("Pending");
        }
    return s;
    }
