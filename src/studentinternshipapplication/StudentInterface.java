void addToDatabase(Student s){                                      //to add the data to database 
    boolean successfulAddition;
     DBOperations db = new DBOperations();
     successfulAddition = db.addStudent(s);
     if (successfulAddition){
         JOptionPane.showMessageDialog(this, "Added Successfully");
         allowChange(false);                                                    //disabling the editing feature of text boxes
         JOptionPane.showMessageDialog(this, "You will be directed back to the home page !");
         this.dispose();
         li.setVisible(true);
     }
     else
         JOptionPane.showMessageDialog(this, "Error occured while inserting !!! ");
}
