void fillInfo(Student s) {                                                     // fill the details in the GUI for a given student 
        txtAddress.setText(s.getAddress());
        txtBirthday.setText(s.getBirthday());
        txtDescription.setText(s.getDescription());
        txtEmail.setText(s.getEmail());
        txtGPA.setText(Float.toString(s.getGPA()));
        txtLanguages.setText(s.getLanguages());
        txtMobileNumber.setText(s.getTelephone());
        txtName.setText(s.getName());
        txtPassword.setText(s.getPassword());
        txtSkills.setText(s.getSkills());
        txtxID.setText(s.getID());
        txtxNIC.setText(s.getNIC());
        comboDepartment.setSelectedItem(s.getDepartnment());
        comboGender.setSelectedItem(s.getGender());
        if ("Pending".equals(s.getAdminAccepted())) {
            lblApproval.setText("Approval Pending");
            lblApproval.setForeground(new Color(255, 255, 5));
            lblOPLsit.setVisible(false);
        } else if ("Accepted".equals(s.getAdminAccepted())) {
            lblApproval.setText("Admin Accepted");
            lblApproval.setForeground(new Color(0, 204, 153));
            lblOPLsit.setVisible(true);
        } else {
            lblApproval.setText("Rejected");
            lblApproval.setForeground(new Color(255, 0, 102));
            lblOPLsit.setVisible(false);
        }
    }
